package khie;

public class Ex03 {
	public static void main(String[] args) {
		
		System.out.println("프로그램 시작");
		System.out.print("java\t");		// 제어문자 \t: tab 
		System.out.print("programming");
		System.out.print("\n프로그램 종료"); 	// \n: 줄바꿈
		
		/*
		 * java에서의 제어문자
		 * \t: 일정한 간격(tab)만큼 들여쓰기를 해주는 제어문자.
		 * \n: 줄바꿈을 제공해 주는 제어문자.
		 */
	}
}
